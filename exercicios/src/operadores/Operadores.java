package operadores;

public class Operadores {
	public static void main(String[] args) {
		//OPERADORES UNÁRIOS
		//exemplo
		a++;
		
		//OPERADORES BINÁRIOS
		//exemplo
		2 + 4;
		
		//OPERADORES TERNÁRIOS
		//exemplo
		3 + 6 - 2/4;
		
//TIPOS CATEGORIA
		//TIPO PREFIX
		//exemplo
		++a;
		
		//TIPO POSTFIX
		//exemplo
		a++;
		
		//TIPO INFIX
		//exemplo
		2 + 3;
	}
}
